namespace TickTickSharp.Models
{
    /// <summary>
    /// Represents the priority level of a task.
    /// </summary>
    public enum TaskPriority
    {
        /// <summary>
        /// No priority assigned.
        /// </summary>
        None = 0,
        
        /// <summary>
        /// Low priority.
        /// </summary>
        Low = 1,
        
        /// <summary>
        /// Medium priority.
        /// </summary>
        Medium = 3,
        
        /// <summary>
        /// High priority.
        /// </summary>
        High = 5
    }
}